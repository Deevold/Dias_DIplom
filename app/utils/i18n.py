def normalize_language(language):
    return language if language in LANGUAGE_OPTIONS else "ru"


def translate(key, language="ru", **kwargs):
    language = normalize_language(language)
    text = TRANSLATIONS.get(language, {}).get(key) or TRANSLATIONS["ru"].get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text


def translate_theme(theme_code, language="ru"):
    return translate(f"theme.{theme_code}", language)


def translate_section(section, language="ru"):
    return translate(f"section.{section}", language)


def translate_level(level, language="ru"):
    return translate(f"level.{level}", language)


def translate_mode(mode, language="ru"):
    return translate(f"mode.{mode}", language)


def translate_bot_level(level, language="ru"):
    return translate(f"bot.{level}", language)


